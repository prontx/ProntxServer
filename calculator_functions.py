# util/calculator_functions.py
# Contains definitions of mathematical functions used for the calculate operations

def add(a, b):
    result = a + b
    return result

def subtract(a, b):
    result = a - b
    return result

def multiply(a, b):
    result = a * b
    return result

def divide(a, b):
    result = a / b
    return result

def divide_no_remainder(a, b):
    result = a // b
    return result

def power(a, b):
    result = a ** b
    return result

def modulo(a, b):
    result = a % b
    return result